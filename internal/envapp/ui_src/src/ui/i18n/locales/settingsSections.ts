import { enUS, type EnvAppTranslationShape } from './en-US';

type AddedSettingsMessages = Pick<EnvAppTranslationShape['settings'], 'configFile' | 'connection'>;
type AddedAccessGateMessages = Pick<EnvAppTranslationShape['accessGate'], 'errors'>;
type AddedSkillsSettingsMessages = Pick<
  EnvAppTranslationShape['skillsSettings'],
  | 'unknown'
  | 'noDescription'
  | 'enabled'
  | 'disabled'
  | 'browse'
  | 'reinstall'
  | 'loadingCatalog'
  | 'noSkillsForFilters'
  | 'shadowedBy'
  | 'table'
  | 'source'
  | 'status'
>;

type AddedLocaleMessages = Readonly<{
  settings: AddedSettingsMessages;
  loggingSettings: EnvAppTranslationShape['loggingSettings'];
  codexSettings: EnvAppTranslationShape['codexSettings'];
  accessGate: AddedAccessGateMessages;
  permissionPolicy: EnvAppTranslationShape['permissionPolicy'];
  skillsSettings: AddedSkillsSettingsMessages;
  codeRuntime: EnvAppTranslationShape['codeRuntime'];
}>;

type DeepPartial<T> = {
  readonly [K in keyof T]?: T[K] extends Readonly<Record<string, unknown>>
    ? DeepPartial<T[K]>
    : T[K];
};

const baseCodeRuntime = enUS.codeRuntime;
const baseLoggingSettings = enUS.loggingSettings;
const baseCodexSettings = enUS.codexSettings;

function loggingSettings(overrides: DeepPartial<EnvAppTranslationShape['loggingSettings']> = {}): EnvAppTranslationShape['loggingSettings'] {
  return {
    ...baseLoggingSettings,
    ...overrides,
  };
}

function codexSettings(overrides: DeepPartial<EnvAppTranslationShape['codexSettings']> = {}): EnvAppTranslationShape['codexSettings'] {
  return {
    ...baseCodexSettings,
    ...overrides,
    rows: { ...baseCodexSettings.rows, ...overrides.rows },
    status: { ...baseCodexSettings.status, ...overrides.status },
    pills: { ...baseCodexSettings.pills, ...overrides.pills },
  };
}

function codeRuntime(overrides: DeepPartial<EnvAppTranslationShape['codeRuntime']> = {}): EnvAppTranslationShape['codeRuntime'] {
  return {
    ...baseCodeRuntime,
    ...overrides,
    source: { ...baseCodeRuntime.source, ...overrides.source },
    rows: { ...baseCodeRuntime.rows, ...overrides.rows },
    notes: { ...baseCodeRuntime.notes, ...overrides.notes },
    removal: { ...baseCodeRuntime.removal, ...overrides.removal },
    operation: { ...baseCodeRuntime.operation, ...overrides.operation },
    stage: { ...baseCodeRuntime.stage, ...overrides.stage },
    prepare: {
      ...baseCodeRuntime.prepare,
      ...overrides.prepare,
      setup: { ...baseCodeRuntime.prepare.setup, ...overrides.prepare?.setup },
      update: { ...baseCodeRuntime.prepare.update, ...overrides.prepare?.update },
      retry: { ...baseCodeRuntime.prepare.retry, ...overrides.prepare?.retry },
    },
    confirm: { ...baseCodeRuntime.confirm, ...overrides.confirm },
    activity: {
      ...baseCodeRuntime.activity,
      ...overrides.activity,
      steps: { ...baseCodeRuntime.activity.steps, ...overrides.activity?.steps },
      failure: { ...baseCodeRuntime.activity.failure, ...overrides.activity?.failure },
    },
    notifications: { ...baseCodeRuntime.notifications, ...overrides.notifications },
  };
}

function addedMessages(args: AddedLocaleMessages): AddedLocaleMessages {
  return args;
}

