function withRussianPluralForms<T>(value: T): T {
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    return value;
  }
  const record = value as Readonly<Record<string, unknown>>;
  if (typeof record.other === 'string') {
    return {
      ...record,
      few: typeof record.few === 'string' ? record.few : record.other,
      many: typeof record.many === 'string' ? record.many : record.other,
    } as T;
  }
  return Object.fromEntries(
    Object.entries(record).map(([key, child]) => [key, withRussianPluralForms(child)]),
  ) as T;
}

