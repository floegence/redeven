type FlowerSurfaceLocale = DeepWidenMessages<typeof flowerSurfaceEnUS>;

type FlowerSurfaceLocaleOverrides = Readonly<{
  host?: Partial<FlowerSurfaceLocale['host']>;
  chat?: Partial<FlowerSurfaceLocale['chat']>;
  threadList?: Partial<FlowerSurfaceLocale['threadList']>;
  emptyState?: Partial<FlowerSurfaceLocale['emptyState']>;
  settings?: Partial<FlowerSurfaceLocale['settings']>;
}>;

function createFlowerSurfaceLocale(overrides: FlowerSurfaceLocaleOverrides): FlowerSurfaceLocale {
  return {
    ...flowerSurfaceEnUS,
    ...overrides,
    host: { ...flowerSurfaceEnUS.host, ...overrides.host },
    chat: { ...flowerSurfaceEnUS.chat, ...overrides.chat },
    threadList: { ...flowerSurfaceEnUS.threadList, ...overrides.threadList },
    emptyState: { ...flowerSurfaceEnUS.emptyState, ...overrides.emptyState },
    settings: { ...flowerSurfaceEnUS.settings, ...overrides.settings },
  };
}

