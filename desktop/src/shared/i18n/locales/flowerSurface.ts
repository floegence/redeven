type FlowerSurfaceLocale = DeepWidenMessages<typeof flowerSurfaceEnUS>;

type FlowerSurfaceLocaleOverrides = Readonly<{
  runtime?: Partial<FlowerSurfaceLocale['runtime']>;
  chat?: Partial<FlowerSurfaceLocale['chat']>;
  threadList?: Partial<FlowerSurfaceLocale['threadList']>;
  emptyState?: Partial<FlowerSurfaceLocale['emptyState']>;
  subagents?: Partial<FlowerSurfaceLocale['subagents']>;
  settings?: Partial<FlowerSurfaceLocale['settings']>;
}>;

function createFlowerSurfaceLocale(overrides: FlowerSurfaceLocaleOverrides): FlowerSurfaceLocale {
  return {
    ...flowerSurfaceEnUS,
    ...overrides,
    runtime: { ...flowerSurfaceEnUS.runtime, ...overrides.runtime },
    chat: { ...flowerSurfaceEnUS.chat, ...overrides.chat },
    threadList: { ...flowerSurfaceEnUS.threadList, ...overrides.threadList },
    emptyState: { ...flowerSurfaceEnUS.emptyState, ...overrides.emptyState },
    subagents: {
      ...flowerSurfaceEnUS.subagents,
      ...overrides.subagents,
      status: { ...flowerSurfaceEnUS.subagents.status, ...overrides.subagents?.status },
      types: { ...flowerSurfaceEnUS.subagents.types, ...overrides.subagents?.types },
      activity: {
        ...flowerSurfaceEnUS.subagents.activity,
        ...overrides.subagents?.activity,
        actions: { ...flowerSurfaceEnUS.subagents.activity.actions, ...overrides.subagents?.activity?.actions },
        titleVerbs: { ...flowerSurfaceEnUS.subagents.activity.titleVerbs, ...overrides.subagents?.activity?.titleVerbs },
        labels: { ...flowerSurfaceEnUS.subagents.activity.labels, ...overrides.subagents?.activity?.labels },
        values: { ...flowerSurfaceEnUS.subagents.activity.values, ...overrides.subagents?.activity?.values },
      },
    },
    settings: { ...flowerSurfaceEnUS.settings, ...overrides.settings },
  };
}

